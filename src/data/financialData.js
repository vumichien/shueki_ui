// 計算関数
export const calculateFinancialMetrics = (data) => {
  // Null check to prevent destructuring errors
  if (!data || typeof data !== 'object') {
    console.error('calculateFinancialMetrics: Invalid data provided', data)
    return {
      grossProfitData: [],
      orderRelatedTotalData: [],
      advertisingPromotionTotalData: [],
      operatingProfitData: []
    }
  }

  const {
    salesData = [],
    costOfSalesData = [],
    shippingData = [],
    paymentFeesData = [],
    advertisingData = [],
    salesPromotionData = [],
    personnelCostsData = [],
    commonCostsData = []
  } = data

  // Additional validation
  if (!Array.isArray(salesData) || salesData.length === 0) {
    console.error('calculateFinancialMetrics: Invalid or empty salesData')
    return {
      grossProfitData: [],
      orderRelatedTotalData: [],
      advertisingPromotionTotalData: [],
      operatingProfitData: []
    }
  }

  // 売上総利益（売上ー売上原価）
  const grossProfitData = salesData.map((sales, index) => 
    sales - (costOfSalesData[index] || 0)
  )

  // 注文連動比合計（送料＋支払い手数料）
  const orderRelatedTotalData = shippingData.map((shipping, index) => 
    shipping + (paymentFeesData[index] || 0)
  )

  // 広告販売促進費合計（広告費＋販売促進費）
  const advertisingPromotionTotalData = advertisingData.map((advertising, index) => 
    advertising + (salesPromotionData[index] || 0)
  )

  // 営業利益（売上ー全ての費用）
  const operatingProfitData = salesData.map((sales, index) => {
    const totalCosts = (costOfSalesData[index] || 0) + 
                      (shippingData[index] || 0) + 
                      (paymentFeesData[index] || 0) + 
                      (advertisingData[index] || 0) + 
                      (salesPromotionData[index] || 0) + 
                      (personnelCostsData[index] || 0) + 
                      (commonCostsData[index] || 0)
    return sales - totalCosts
  })

  return {
    grossProfitData,
    orderRelatedTotalData,
    advertisingPromotionTotalData,
    operatingProfitData
  }
}

// 月間サマリー計算
export const calculateMonthlySummary = (data) => {
  // Null check
  if (!data || typeof data !== 'object' || !Array.isArray(data.salesData)) {
    console.error('calculateMonthlySummary: Invalid data provided', data)
    return {
      totalSales: 0,
      totalOperatingProfit: 0,
      totalAdvertising: 0,
      totalSalesPromotion: 0,
      operatingMargin: null
    }
  }

  const metrics = calculateFinancialMetrics(data)
  
  return {
    totalSales: data.salesData.reduce((sum, value) => sum + (value || 0), 0),
    totalOperatingProfit: metrics.operatingProfitData.reduce((sum, value) => sum + (value || 0), 0),
    totalAdvertising: (data.advertisingData || []).reduce((sum, value) => sum + (value || 0), 0),
    totalSalesPromotion: (data.salesPromotionData || []).reduce((sum, value) => sum + (value || 0), 0),
    operatingMargin: null // 計算後に設定
  }
}

// 営業利益率計算
export const calculateOperatingMargin = (totalSales, totalOperatingProfit) => {
  return totalSales > 0 ? Math.round((totalOperatingProfit / totalSales) * 100) : 0
}

